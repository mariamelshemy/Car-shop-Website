            <br>
            <br>
            <p> <b>The 1.5 turbo engine generates 160 horsepower and a powerful performance placing the car at the
                top.</b></p>
            <br>
            <p>The Egyptian International Trading & Agencies Co. (EIT), the exclusive agent for Kia Motors in Egypt,
              announced the launching of the new Kia XCeed for the first time in Egypt, following the successes achieved
              by the car in European countries. XCeed appear for the first time in the Middle East and North Africa
              through the Egyptian market. This is a Sequel for Kia's success story in the Egyptian, as it is the most
              popular and one from the top-selling brands in Egypt.</p>
            <p>
              Launching of The New Kia XCeed demonstrate the vision of KIA global, which is always exceeding
              expectations by Suppling the Egyptian market with new models that are closest to consumers, providing
              consumers with new experiences for enjoyable and safe driving based on innovative technologies. KIA XCeed
              is designed as a multi-usage sports car, the XCeed car meets the changing needs of the Egyptian market
              combining design, safety, luxury and affordable prices.
            </p>
            <p>Kia XCeed which was produced in Kia factories in Slovakia, joins the Kia family in Egypt with amazing
              reliable features and the highest standards of safety and luxury, using high technology that provides
              quality in all aspects of the car, confirming Kia’s continuous efforts to provide everything that is new
              for the Egyptian market</p>
            <p><b>Turbocharged engines and powertrains:</b></p>
            <p> The Kia XCeed is powered by a turbocharged engine. The high-power 1.5-litre T-GDi boosting a 160 Horse
              power at 5,500 RPM and 253 NM torque at 1,500 RPM, which ensures high acceleration and power with an
              average fuel consumption of 6.1 liters/100k.m; a fuel tank capacity of 50 liters that perfectly matches
              the car’s ability to thrive in a variety of driving situations. The 1.5-litre T-GDi enables the Kia XCeed
              to accelerate from 0-to-100 km in 9.2 seconds with 7 Speed Dual Clutch Transmission. Its MacPherson strut
              and Multi-link suspension enhances comfort for all passengers making a smoother, safer drive with an
              excellent versatility.</p>
            <p><b>Safety and security:</b></p>
            <P> The New KIA XCeed received 5 stars in the European safety tests EURONCAP through a high safety system.
              All XCeed classes are equipped with 6 airbags distributed as follows: driver and front passenger airbags,
              side airbags and curtain airbags; engine Immobilizer; Anti-lock Braking System (ABS); and Electronic
              Brake-force Distribution (EBD). The car is also equipped with 15-inch drums to ensure the highest safety
              while driving and controlling the car, Hill-start Assist Control (HAC), Electronic Stability Control
              (ESC), and Boost System, vehicle stability with steering wheel VSM, hill stabilization program DAC, rear
              parking assist system Rear Sensor, and ISOFIX child seat in the rear seats.</P>
            <p><b>A sporty design, urban alternative to the traditional SUV:</b></p>
            <p>The new model will be offered in a choice of up to 8 paint finishes for the exterior, with a vibrant new
              Quantum Yellow shade that is unique to XCeed. The Kia XCeed sits on an 18-inch machined aluminum alloy
              wheels, shod with high traction 235/45 R18 tires which provides the highest levels of stability for an
              SUV.

              Mirrors feature electric adjustment, as electric and heated folding mirrors rotate out to extend your
              lines of sight. With LED daytime running lights, visibility will be decisively improved during the day and
              the consumption-related disadvantages of driving with the low beam switched on will also be avoided. In
              addition, LED rear combination lamps keep drivers safe while driving backwards, stopping or driving in fog
              conditions, and a radiator grille finished in chrome and high-gloss.

              As for the dimensions of the car, it is 3,945 mm in length, 1,826 mm in width and 1,495 mm in height, with
              a wide wheelbase of 2,650 mm making the car more balanced and stable on the roads.</p>
            <p><b>The cabin and storage space of an SUV in a sportier, more compact body:</b></p>
            <p>
              Manufactured with an elegant interior design, storage space and ample interior leg and head-room for front
              and back passengers. With the car’s longer rear overhang creating a longer boot, the storage capacity of
              the luggage compartment is 426 liters. The rear seats fold down to increase the luggage capacity to 1,394
              liters, and there are other multiple uses through the 60:40 ratio of foldable rear seat; to allow the
              customer extra versatility, comfort, and luxury the opening and closing of the luggage boot is done
              electrically.
            </p>


